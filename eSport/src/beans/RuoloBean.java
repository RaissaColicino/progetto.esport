package beans;

public class RuoloBean {
	public RuoloBean() {
		
	}
	
	public String getUsr() {
		return usr;
	}
	
	public void setUsr(String usr) {
		this.usr = usr;
	}
	
	public String getPermesso() {
		return permesso;
	}
	
	public void setPermesso(String permesso) {
		this.permesso = permesso;
	}

	private String usr;
	private String permesso;
}
